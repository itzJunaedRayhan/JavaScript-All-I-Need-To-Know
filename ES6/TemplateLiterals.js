//  Template Literals

let a = 5;
let b = 6;

//  oldest way
console.log("I am " + a + " and I am " + b);

//  Using Template Literals
console.log(`I am ${a} and I am ${b}`);

//  we can use expression and statement here
console.log(`I am ${a + b}`);

//  another example
console.log(`I am test
            this is a test`);
